const shopEl = document.querySelectorAll(".shop__item");
const modalEl = document.querySelector(".modal");

shopEl.forEach((item) =>
  item.addEventListener("click", () => {
    const dataIndex = item.dataset.index;
    openModal(data[dataIndex]);
  })
);
async function openModal(product) {
  let selectedSize = "s";
  const selectedAdditives = [];

  function updateTotalPrice() {
    const sizePrice = parseFloat(product.sizes[selectedSize]["add-price"]) || 0;
    const additivesPrice = selectedAdditives.length * 0.5;
    const totalPrice = parseFloat(product.price) + sizePrice + additivesPrice;

    document.querySelector(
      ".total__price"
    ).textContent = `$${totalPrice.toFixed(2)}`;
  }

  modalEl.classList.add("modal--show");
  document.body.classList.add("stop--scrolling");
  modalEl.innerHTML = `
    <div class="modal__card">
      <img src="${product.img}" alt="itemPhoto" class="modal__items-backdrop" />
      <div class="modal__card-info">
      <div >
      <h2 class="modal__item-title">${product.name}</h2>
        <p class="modal__item-description">${product.description}</p>
      </div>        
        <div class='modal__item-size '>
          <p>Size</p>    
          <ul class="modal__additions-options"> 
            ${Object.entries(product.sizes)
              .map(
                ([key, value]) => `
                  <li class="modal__additions-option ${
                    selectedSize === key ? "modal_size-active" : ""
                  }" data-size="${key}">
                    <span class="modal__counter">${key}</span>
                    <span class="modal__size">${value.size}</span>
                  </li>`
              )
              .join("")}
          </ul>
        </div>
        <div class="modal__additions-wrapper">  
          <p>Additives</p>      
          <ul class="modal__additions-options item__options">                
            ${product.additives
              .map(
                (additive, index) => `
                  <li class="modal__additions-option ${
                    selectedAdditives.includes(index)
                      ? "modal_additive-active"
                      : ""
                  }" data-additive-index="${index}">
                    <span class="modal__counter">${index + 1}</span> 
                    <span class="modal__size">${additive.name}</span>   
                  </li>`
              )
              .join("")}         
          </ul>
        </div>
        <div class="total__price-wrapper">
          <p class="total__price-title">Total:</p>
          <p class="total__price">$${parseFloat(product.price).toFixed(
            2
          )}</p>        
        </div>
        <p class="modal__info-app">The cost is not final. Download our mobile app to see the final price and place your order. Earn loyalty points and enjoy your favorite coffee with up to 20% discount.</p>    
        <button type="button" class="modal__button-close">Close</button>
      </div>  
    </div>
  `;

  document
    .querySelectorAll(".modal__additions-option[data-size]")
    .forEach((sizeOption) => {
      sizeOption.addEventListener("click", () => {
        selectedSize = sizeOption.dataset.size;
        updateTotalPrice();
        document
          .querySelectorAll(".modal__additions-option[data-size]")
          .forEach((option) => option.classList.remove("modal_size-active"));
        sizeOption.classList.add("modal_size-active");
      });
    });

  document
    .querySelectorAll(".modal__additions-option[data-additive-index]")
    .forEach((additiveOption) => {
      additiveOption.addEventListener("click", () => {
        const additiveIndex = parseInt(additiveOption.dataset.additiveIndex);
        const existingIndex = selectedAdditives.indexOf(additiveIndex);

        if (existingIndex === -1) {
          selectedAdditives.push(additiveIndex);
        } else {
          selectedAdditives.splice(existingIndex, 1);
        }

        updateTotalPrice();

        additiveOption.classList.toggle("modal_additive-active");
      });
    });

  const btnClose = document.querySelector(".modal__button-close");
  btnClose.addEventListener("click", () => closeModal());
}

function closeModal() {
  modalEl.classList.remove("modal--show");
  document.body.classList.remove("stop--scrolling");
}

function closeModal() {
  modalEl.classList.remove("modal--show");
  document.body.classList.remove("stop--scrolling");
}
window.addEventListener("click", (e) => {
  if (e.target === modalEl) {
    closeModal();
  }
});
window.addEventListener("keydown", (e) => {
  if (e.keyCode === 27) {
    closeModal();
  }
});
